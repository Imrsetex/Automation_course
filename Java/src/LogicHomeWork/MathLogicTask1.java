package PDFfiller_Automation.Lesson_4;

public class MathLogicTask1 {
    public static void main(String[] args) {
        // Даны 4 числа типа int. Сравнить их и вывести наименьшее на консоль.
        int a = 5, b = 2, c = 3, d = 4;

        if (a <= 4) {
            System.out.println("Max num is: " + a);
        } else if (b <= 4) {
            System.out.println("Max num is: " + b);
        } else if (c <= 4) {
            System.out.println("Max num is: " + c);
        } else if (d <= 4) {
            System.out.println("Max num is: " + d);
        }
    }
}
